package ControlStatement;

public class SwitchStatement {
	public static void main(String[] args) {
		
	}
	public void switchCase() {
		int i = 2;
		
		switch(i) {
		  	case 1 :
		  		System.out.print(" Value 1 ");
		  		break;
		  	case 2 :
		  		System.out.print(" Value 2 ");
		  		break;
		  	case 3 :
		  		System.out.print(" Value 3 ");
		  		break;
		  	default :
		  		System.out.print(" Value 3 ");
		  		break;
		}
	}

}
